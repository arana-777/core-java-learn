public class IfElse {

	public static void main(String[] args) {
		int marks = 55;
		if (marks > 65) {
			System.out.println("First division");
		} else if (marks <=65) {
			System.out.println("Second division");
		} else {
			System.out.println("Third division");
		}

	}

}

package basics_of_java.data_types;

public class Array_types {

	public static void main(String[] args) {
		// declaration of an array
		int[] arr1;
		
		// initialization of an array
		arr1 = new int[5];
		for (var i: arr1) System.out.println(i);

		// another direct approach for above 2 steps
		int arr2[] = new int[5];
		for (var i: arr2) System.out.print(i + " : ");
		
		
		
	}

}

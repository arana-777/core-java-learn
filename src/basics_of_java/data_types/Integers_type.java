package basics_of_java.data_types;

public class Integers_type {
	public static void main(String[] args) {
		// byte type = 1bute (8 bits)
		byte b = 20;
		System.out.println("byte type, b = " + b);
		
		// short type = 2 bytes (16 bits)
		short s = 20;
		System.out.println("short type, s = " + s);
		
		// int type = 4 bytes (32 bits)
		int i = 20;
		System.out.println("integer type, i = " + i);
		
		// long type = 8 bytes (64 bits)
		long l = 20;
		System.out.println("long type, l = " + l);
		
		
		
	}
}

package basics_of_java.data_types;

public class Char_and_Boolean {

	public static void main(String[] args) {
		// char type = 2 bytes (16 bits)
		char ch = 'S';
		System.out.println("Char type, ch = " + ch);
		
		// boolean types
		boolean t = true;
		boolean f = false;
		System.out.println("t = " + t);
		System.out.print("f = " + f);
		
		

	}

}

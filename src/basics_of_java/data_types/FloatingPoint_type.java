package basics_of_java.data_types;

public class FloatingPoint_type {

	public static void main(String[] args) {
		// float type = 4bytes(32bits)
		float f = 20.25f;
		System.out.println("float type, f = " + f);
		
		// double type = 8 bytes (64 bits)
		double d = 20.25;
		System.out.println("double type, d = " + d);

	}

}
